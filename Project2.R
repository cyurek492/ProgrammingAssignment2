makeCacheMatrix <- function(x = matrix()){
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
}
  get <- function() x
  setinverse <- function(inverse) m <<- inverse
  getinverse <- function() m 
  list(set=set,
       get=get, 
       setinverse=setinverse, 
       getinverse=getinverse)
}

cacheSolve <- function(x=matrix(), ...){
  m <- x$getinverse()
  if(!is.null(m)){
    message("Getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  return(m)
}


